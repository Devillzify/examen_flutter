import 'package:examen_flutter/pages/home.dart';
import 'package:examen_flutter/pages/list.dart';
import 'package:examen_flutter/preferences/preferences.dart';
import 'package:flutter/material.dart';
import 'package:provider/provider.dart';

void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Preferences.init();
  runApp(MultiProvider(providers: [
    ChangeNotifierProvider(create: (_) => ChangeNotifier()),
  ], child: MyApp()));
}

class MyApp extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      title: 'Simulacre',
      initialRoute: 'home',
      routes: {
        'home': (_) => HomePage(),
        'list': (_) => ListPage(),
      },
      theme: ThemeData(
        // No es pot emprar colorPrimary des de l'actualitzaci√≥ de Flutter
        colorScheme: ColorScheme.light().copyWith(
          primary: Colors.deepPurple,
        ),
        floatingActionButtonTheme: FloatingActionButtonThemeData(
          backgroundColor: Colors.deepPurple,
        ),
      ),
    );
  }
}
